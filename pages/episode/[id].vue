        '1x6: Sexto episodio',
        '1x7: Séptimo episodio',
        '1x8: Octavo episodio',
        '1x9: Noveno episodio',
        '1x10: Décimo episodio',
    ];
    episodeData.value = episodes[episodeId - 1];
});
</script>
